select
  '08_clients_policies' as audit_section,
  p.polname as policy_name,
  case p.polcmd
    when 'r' then 'SELECT'
    when 'a' then 'INSERT'
    when 'w' then 'UPDATE'
    when 'd' then 'DELETE'
    when '*' then 'ALL'
    else p.polcmd::text
  end as command,
  case when p.polpermissive then 'PERMISSIVE' else 'RESTRICTIVE' end as policy_mode,
  case
    when p.polroles = '{0}'::oid[] then array['public']
    else (
      select array_agg(r.rolname order by r.rolname)
      from unnest(p.polroles) role_oid
      join pg_roles r on r.oid = role_oid
