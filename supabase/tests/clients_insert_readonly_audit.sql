select
  '02_clients_reconstructed_ddl' as audit_section,
  format(
    'create table %I.%I (%s%s%s);',
    n.nspname,
    c.relname,
    coalesce((
      select string_agg(
        format(
          E'\n  %I %s%s%s%s%s',
          a.attname,
          pg_catalog.format_type(a.atttypid, a.atttypmod),
          case
            when a.attidentity = 'a' then ' generated always as identity'
            when a.attidentity = 'd' then ' generated by default as identity'
            else ''
          end,
          case
            when a.attgenerated <> '' then ' generated always as (' || pg_get_expr(d.adbin, d.adrelid) || ') stored'
            when d.adbin is not null and a.attidentity = '' then ' default ' || pg_get_expr(d.adbin, d.adrelid)
            else ''
          end,
          case when a.attnotnull then ' not null' else '' end,
          case when a.attcollation <> t.typcollation and a.attcollation <> 0 then ' collate ' || quote_ident(coll.collname) else '' end
        ),
        ','
        order by a.attnum
      )
      from pg_attribute a
      join pg_type t on t.oid = a.atttypid
      left join pg_attrdef d on d.adrelid = a.attrelid and d.adnum = a.attnum
      left join pg_collation coll on coll.oid = a.attcollation
      where a.attrelid = c.oid
        and a.attnum > 0
        and not a.attisdropped
    ), ''),
    coalesce((
      select string_agg(
        format(E',\n  constraint %I %s', con.conname, pg_get_constraintdef(con.oid, true)),
        ''
        order by con.conname
      )
      from pg_constraint con
      where con.conrelid = c.oid
    ), ''),
    E'\n'
  ) as reconstructed_ddl
from pg_class c
join pg_namespace n on n.oid = c.relnamespace
where n.nspname = 'public'
  and c.relname = 'clients';

