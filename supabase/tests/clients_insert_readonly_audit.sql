  case when sequence_name is null then null else has_sequence_privilege('authenticated', sequence_name, 'USAGE') end as authenticated_usage,
  case when sequence_name is null then null else has_sequence_privilege('authenticated', sequence_name, 'SELECT') end as authenticated_select,
